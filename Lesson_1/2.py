# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.
a = 5
b = 6
print('И a & b :', a & b)
print('ИЛИ a | b :', a | b)
print('ИСКЛЮЧАЮЩЕЕ ИЛИ a ^ b :', a ^ b)
print('ПОБИТОВЫЙ СДВИГ ВПРАВО a >> 2 :', a >> 2)
print('ПОБИТОВЫЙ СДВИГ ВЛЕВО a << 2 :', a << 2)
